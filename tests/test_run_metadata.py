# Test 6: smoke check does not crash on B LAB rows
# ---------------------------------------------------------------------------

class TestSmokeCheckBLab:
    def test_smoke_check_does_not_crash_with_b_lab_rows(self, capsys):
        """_smoke_check_b_lab must complete without raising when B LAB rows present."""
        from scripts.run_cli import _smoke_check_b_lab

        df = _b_lab_df()
        mapping = _minimal_mapping()
        config = _minimal_config()
        result = cluster_suppliers(df, mapping, config)
        # Should not raise
        _smoke_check_b_lab(result, mapping)
        captured = capsys.readouterr()
        # Must print at least one [SMOKE] line
        assert "[SMOKE]" in captured.out, (
            "Expected [SMOKE] lines in stdout when B LAB rows are present"
        )

    def test_smoke_check_noop_without_b_lab_rows(self, capsys):
        """_smoke_check_b_lab must be silent when no B LAB rows are present."""
        from scripts.run_cli import _smoke_check_b_lab

        df = pl.DataFrame({
            "Supplier Name": ["ACME CORP INC", "GLOBAL TECH LLC"],
            "Address": ["100 MAIN ST", "200 BROADWAY"],
            "Country": ["US", "US"],
        })
        mapping = {"supplier_name": "Supplier Name", "address": "Address", "country": "Country"}
        config = _minimal_config()
        result = cluster_suppliers(df, mapping, config)
        _smoke_check_b_lab(result, mapping)
        captured = capsys.readouterr()
        assert "[SMOKE]" not in captured.out, (
            "Smoke check must not print when no B LAB rows are present"
        )

    def test_smoke_check_reports_cluster_outcome(self, capsys):
        """Smoke check must report whether B LAB rows ended up in the same cluster."""
        from scripts.run_cli import _smoke_check_b_lab

        df = _b_lab_df()
        mapping = _minimal_mapping()
        config = _minimal_config()
        result = cluster_suppliers(df, mapping, config)
        _smoke_check_b_lab(result, mapping)
        captured = capsys.readouterr()
        # Must contain either a success or failure indicator
        assert ("✅ Clustered together" in captured.out or
                "❌ B LAB rows NOT" in captured.out), (
            f"Smoke check must report cluster outcome. Got:\n{captured.out}"
        )
