class TestSaveRunMetadata:
    def test_writes_valid_json(self, tmp_path):
        config = _minimal_config()
        meta = build_run_metadata(
            mapping=_minimal_mapping(), input_row_count=5, config=config
        )
        path = str(tmp_path / "run_metadata.json")
        save_run_metadata(meta, path)
        assert os.path.isfile(path)
        with open(path) as fh:
            loaded = json.load(fh)
        assert loaded["mapped_supplier_name"] == "CommonName"
        assert loaded["mapped_address"] == "Address Line 1"
        assert loaded["input_row_count"] == 5

    def test_creates_parent_directories(self, tmp_path):
        config = _minimal_config()
        meta = build_run_metadata(mapping={}, input_row_count=0, config=config)
        nested = str(tmp_path / "a" / "b" / "c" / "run_metadata.json")
        save_run_metadata(meta, nested)
        assert os.path.isfile(nested)


