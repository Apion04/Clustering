# Test 2: build_run_metadata captures all required fields
# ---------------------------------------------------------------------------

class TestBuildRunMetadata:
    def test_required_keys_present(self):
        config = _minimal_config()
        meta = build_run_metadata(
            mapping=_minimal_mapping(),
            input_row_count=100,
            config=config,
        )
        required = [
            "app_commit_hash", "app_version", "run_timestamp",
            "input_row_count",
            "mapped_supplier_name", "mapped_address",
            "mapped_city", "mapped_country", "mapped_postal_code",
            "mapped_email", "mapped_tax_id", "mapped_website",
            "ignore_client_domains", "show_70_candidates",
            "llm_mode", "review_output_enabled", "audit_output_enabled",
        ]
        for key in required:
            assert key in meta, f"run_metadata missing required key: {key!r}"

    def test_mapping_values_recorded_correctly(self):
        config = _minimal_config()
        mapping = _minimal_mapping()
        meta = build_run_metadata(mapping=mapping, input_row_count=42, config=config)
        assert meta["mapped_supplier_name"] == "CommonName"
        assert meta["mapped_address"] == "Address Line 1"
        assert meta["mapped_city"] == "Supplier Origin City"
        assert meta["mapped_country"] == "Supplier Origin Country"
        assert meta["mapped_postal_code"] == "Supplier Origin Postal"
        assert meta["input_row_count"] == 42

    def test_missing_address_column_recorded_as_empty(self):
        """If no address column is in the mapping, mapped_address must be ''."""
        config = _minimal_config()
        mapping = {"supplier_name": "CommonName", "country": "Country"}
        meta = build_run_metadata(mapping=mapping, input_row_count=10, config=config)
        assert meta["mapped_address"] == "", (
            f"Expected mapped_address='', got {meta['mapped_address']!r}"
        )

    def test_app_version_non_empty(self):
        config = _minimal_config()
        meta = build_run_metadata(mapping={}, input_row_count=0, config=config)
        assert meta["app_version"], "app_version must be non-empty"

    def test_ignore_domains_serialised_as_sorted_list(self):
        config = _minimal_config()
        config.ignore_client_domains = frozenset({"zebra.com", "apple.com"})
        meta = build_run_metadata(mapping={}, input_row_count=0, config=config)
        assert isinstance(meta["ignore_client_domains"], list)
        assert meta["ignore_client_domains"] == sorted(["zebra.com", "apple.com"])

    def test_show_70_candidates_is_bool(self):
        """show_70_candidates must be a bool (not None, not a string)."""
        config = _minimal_config()
        meta = build_run_metadata(mapping={}, input_row_count=0, config=config)
        assert isinstance(meta["show_70_candidates"], bool)

    def test_show_70_candidates_reflects_config(self):
        """show_70_candidates value must match what was set in config."""
        config = _minimal_config()
        config.allow_unresolved_llm_candidates_in_final_output = False
        meta_off = build_run_metadata(mapping={}, input_row_count=0, config=config)
        assert meta_off["show_70_candidates"] is False

        config.allow_unresolved_llm_candidates_in_final_output = True
        meta_on = build_run_metadata(mapping={}, input_row_count=0, config=config)
        assert meta_on["show_70_candidates"] is True


