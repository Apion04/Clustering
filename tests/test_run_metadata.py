# Test 4: run_metadata.json generated alongside final output (pipeline-level)
# ---------------------------------------------------------------------------

class TestRunMetadataPipelineGeneration:
    def test_run_metadata_generated_in_output_dir(self, tmp_path):
        """After cluster_suppliers, saving run_metadata produces a valid file."""
        df = _b_lab_df()
        mapping = _minimal_mapping()
        config = _minimal_config()
        result = cluster_suppliers(df, mapping, config)
        meta = build_run_metadata(
            mapping=mapping,
            input_row_count=len(df),
            config=config,
        )
        meta_path = str(tmp_path / "run_metadata.json")
        save_run_metadata(meta, meta_path)
        assert os.path.isfile(meta_path)
        with open(meta_path) as fh:
            loaded = json.load(fh)
        assert loaded["mapped_supplier_name"] == "CommonName"
        assert loaded["mapped_address"] == "Address Line 1"
        assert loaded["input_row_count"] == 3

    def test_run_metadata_timestamp_is_iso_string(self, tmp_path):
        config = _minimal_config()
        meta = build_run_metadata(mapping={}, input_row_count=0, config=config)
        meta_path = str(tmp_path / "meta.json")
        save_run_metadata(meta, meta_path)
        with open(meta_path) as fh:
            loaded = json.load(fh)
        ts = loaded["run_timestamp"]
        assert "T" in ts, f"Expected ISO timestamp, got {ts!r}"


