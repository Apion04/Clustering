# Test 5: final_supplier_clustered.csv columns are unchanged
# ---------------------------------------------------------------------------

class TestFinalOutputColumnsUnchanged:
    def test_no_internal_columns_leak_into_main_df(self):
        """cluster_suppliers must not add any columns beyond original + Cluster Number + Match Percentage."""
        df = _b_lab_df()
        mapping = _minimal_mapping()
        config = _minimal_config()
        result = cluster_suppliers(df, mapping, config)
        main_df = result["main_df"]
        input_cols = set(df.columns)
        allowed_extra = {"Cluster Number", "Match Percentage"}
        extra_cols = set(main_df.columns) - input_cols - allowed_extra
        assert not extra_cols, (
            f"Internal columns leaked into main_df: {extra_cols}"
        )

    def test_main_df_has_correct_column_order_prefix(self):
        """Original columns must precede Cluster Number and Match Percentage."""
        df = _b_lab_df()
        mapping = _minimal_mapping()
        config = _minimal_config()
        result = cluster_suppliers(df, mapping, config)
        cols = result["main_df"].columns
        # Original input columns must all be present
        for c in df.columns:
            assert c in cols, f"Original column {c!r} missing from main_df"


